function sum(n1:number, n2:number): number{
    return n1 + n2
}
function menos(n1:number, n2: number) : number {
    return n1-n2
}
function mult(n1:number, n2: number) : number {
    return n1*n2
}
function divisão(n1:number, n2: number) : number {
    return n1/n2
}
export {sum, menos, mult, divisão}